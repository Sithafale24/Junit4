package org.example;

public class MyCalculator {
    public  int sum(int a,int b)
    {
        return a + b;
    }
    public  int sub(int a,int b)
    {
        return a - b;
    }

    public  int Mup(int a,int b)
    {
        return a * b;
    }
    public  int Div(int a,int b)
    {
        return a / b;
    }


}
